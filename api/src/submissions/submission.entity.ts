import {
  Entity,
  PrimaryGeneratedColumn,
  Column,
  ManyToOne,
  CreateDateColumn,
  UpdateDateColumn,
} from 'typeorm';
import { User } from '../users/user.entity';
import { Pit } from '../pits/pit.entity';

export enum SubmissionStatus {
  PENDING = 'PENDING',
  RUNNING = 'RUNNING',
  DONE = 'DONE',
  ERROR = 'ERROR',
}

@Entity('submissions')
export class Submission {
  @PrimaryGeneratedColumn('uuid')
  id: string;

  @ManyToOne(() => User, (user) => user.submissions, { eager: false })
  user: User;

  @ManyToOne(() => Pit, (pit) => pit.submissions, { eager: false })
  pit: Pit;

  @Column()
  s3Key: string;

  @Column({
    type: 'enum',
    enum: SubmissionStatus,
    default: SubmissionStatus.PENDING,
  })
  status: SubmissionStatus;

  // Runner score (0-100) or null if pending
  @Column({ type: 'float', nullable: true })
  score: number | null;

  // Structured feedback from runner: { testsPassed, testsFailed, messages, ... }
  @Column({ type: 'jsonb', nullable: true })
  feedback: Record<string, unknown> | null;

  // S3 key pointing to logs generated by the runner (stdout/stderr)
  @Column({ type: 'text', nullable: true })
  logsS3Key: string | null;

  @CreateDateColumn()
  createdAt: Date;

  @UpdateDateColumn()
  updatedAt: Date;
}
